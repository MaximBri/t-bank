import { describe, expect, it } from 'vitest'
import { formatDateRange } from '@/entities/event/lib/format-date-range'

describe('formatDateRange', () => {
  describe('без даты окончания', () => {
    it('форматирует дату начала без конца', () => {
      expect(formatDateRange('2024-06-15T00:00:00')).toBe('С 15 июня 2024')
    })

    it('работает с разными месяцами', () => {
      expect(formatDateRange('2024-01-01T00:00:00')).toBe('С 1 января 2024')
      expect(formatDateRange('2024-12-31T00:00:00')).toBe('С 31 декабря 2024')
    })

    it('работает без временной части', () => {
      expect(formatDateRange('2025-03-10')).toBe('С 10 марта 2025')
    })
  })

  describe('одинаковый год и месяц', () => {
    it('показывает диапазон дней в одном месяце', () => {
      expect(formatDateRange('2024-06-01T00:00:00', '2024-06-15T00:00:00')).toBe('1 - 15 июня 2024')
    })

    it('начало и конец в один день', () => {
      expect(formatDateRange('2024-08-20T00:00:00', '2024-08-20T00:00:00')).toBe(
        '20 - 20 августа 2024',
      )
    })
  })

  describe('одинаковый год, разные месяцы', () => {
    it('показывает диапазон месяцев в одном году', () => {
      expect(formatDateRange('2024-05-10T00:00:00', '2024-09-20T00:00:00')).toBe(
        '10 мая - 20 сентября 2024',
      )
    })

    it('январь — декабрь одного года', () => {
      expect(formatDateRange('2024-01-01T00:00:00', '2024-12-31T00:00:00')).toBe(
        '1 января - 31 декабря 2024',
      )
    })
  })

  describe('разные годы', () => {
    it('показывает полный диапазон с двумя годами', () => {
      expect(formatDateRange('2023-11-15T00:00:00', '2024-02-20T00:00:00')).toBe(
        '15 ноября 2023 - 20 февраля 2024',
      )
    })

    it('большой разброс по годам', () => {
      expect(formatDateRange('2020-01-01T00:00:00', '2025-12-31T00:00:00')).toBe(
        '1 января 2020 - 31 декабря 2025',
      )
    })
  })

  describe('все месяцы на русском', () => {
    const months = [
      'января',
      'февраля',
      'марта',
      'апреля',
      'мая',
      'июня',
      'июля',
      'августа',
      'сентября',
      'октября',
      'ноября',
      'декабря',
    ]

    months.forEach((month, index) => {
      const monthNum = String(index + 1).padStart(2, '0')
      it(`корректно отображает месяц ${month}`, () => {
        expect(formatDateRange(`2024-${monthNum}-01`)).toContain(month)
      })
    })
  })
})
